package basicprograms;

public class Encapsulation {
	int i;
	String name;
	char a;

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public char getA() {
		return a;
	}

	public void setA(char a) {
		this.a = a;
	}

}
